int		ft_recursive_power(int nb, int power)
{
	if (power == 0)
		return (1);
	if (power < 0)
		return (0);
	if (power == 1)
		return (nb);
	if (power > 64 && nb >= 2)
		return (-1);
	return (nb * ft_recursive_power(nb, power - 1));
}
